package cc.cyberdark.consolecoatcraft.tests;

import static org.junit.Assert.*;
import org.junit.Test;

import cc.cyberdark.flags.CoatOfArms;
import cc.cyberdark.flags.VerticalFlag;

public class VerticalFlagTest {
    
    /**
     * Test to ensure that the generate() method of VerticalFlag returns a non-null result.
     * 
     * This test creates a new instance of VerticalFlag with specific parameters and then
     * calls the generate() method. It verifies that the result is not null, indicating
     * that the generation process was successful.
     */
    @Test
    public void testGenerateReturnsNotNull() {
        // Arrange: Create an instance of CoatOfArms and HorizontalFlag with specific parameters
        CoatOfArms coatOfArms = new CoatOfArms(16, "dolphin", "black", "center", "middle");
        VerticalFlag verticalFlag = new VerticalFlag(20, 10, coatOfArms, "red", "white", "blue");
        
        // Act: Call the generate() method
        String[] result = verticalFlag.generate();

        // Assert: Verify that the result is not null
        assertNotNull(result);
    }

    /**
     * Test to ensure that the length of the result array generated by VerticalFlag is as expected.
     * 
     * This test creates a new instance of CoatOfArms and VerticalFlag with specific parameters and then
     * calls the generate() method. It verifies that the length of the result array is as expected.
     */
    @Test
    public void testGenerateResultLengthIsExpected() {
        // Arrange: Create an instance of CoatOfArms and VerticalFlag with specific parameters
        CoatOfArms coatOfArms = new CoatOfArms(16, "dolphin", "black", "center", "middle");
        VerticalFlag verticalFlag = new VerticalFlag(64, 24, coatOfArms, "red", "white", "blue");
        
        // Act: Call the generate() method
        String[] result = verticalFlag.generate();

        // Assert: Verify that the length of the result array is as expected
        assertEquals(24, result.length);
    }
}
